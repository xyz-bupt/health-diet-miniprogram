/**
 * 食物数据库对象
 * 包含所有分类数据和查询方法
 */
const foodDatabase = {
  // 各分类数据
  grains,
  vegetables,
  fruits,
  meat,
  seafood,
  dairy,
  beans,
  nuts,
  fats,
  beverages,
  snacks,
  condiments,

  /**
   * 获取所有食物列表
   * @returns {array} 所有食物的数组
   */
  getAllFoods() {
    return [
      ...this.grains,
      ...this.vegetables,
      ...this.fruits,
      ...this.meat,
      ...this.seafood,
      ...this.dairy,
      ...this.beans,
      ...this.nuts,
      ...this.fats,
      ...this.beverages,
      ...this.snacks,
      ...this.condiments
    ];
  },

  /**
   * 根据ID查找食物
   * @param {string} id - 食物ID
   * @returns {object|null} 食物对象，未找到返回null
   */
  getFoodById(id) {
    const allFoods = this.getAllFoods();
    return allFoods.find(food => food.id === id) || null;
  },

  /**
   * 根据分类查找食物
   * @param {string} category - 分类名称
   * @returns {array} 该分类下的食物列表
   */
  getFoodsByCategory(category) {
    if (this[category]) {
      return this[category];
    }
    return [];
  },

  /**
   * 搜索食物
   * @param {string} keyword - 搜索关键词
   * @returns {array} 匹配的食物列表
   */
  searchFoods(keyword) {
    const allFoods = this.getAllFoods();
    if (!keyword) return allFoods;

    const lowerKeyword = keyword.toLowerCase();
    return allFoods.filter(food =>
      food.name.toLowerCase().includes(lowerKeyword) ||
      food.category.toLowerCase().includes(lowerKeyword) ||
      food.subcategory.toLowerCase().includes(lowerKeyword)
    );
  },

  /**
   * 获取食物分类统计
   * @returns {object} 各分类的食物数量
   */
  getCategoryStats() {
    return {
      grains: this.grains.length,
      vegetables: this.vegetables.length,
      fruits: this.fruits.length,
      meat: this.meat.length,
      seafood: this.seafood.length,
      dairy: this.dairy.length,
      beans: this.beans.length,
      nuts: this.nuts.length,
      fats: this.fats.length,
      beverages: this.beverages.length,
      snacks: this.snacks.length,
      condiments: this.condiments.length,
      total: this.getAllFoods().length
    };
  }
};

// CommonJS 导出
module.exports = foodDatabase;
